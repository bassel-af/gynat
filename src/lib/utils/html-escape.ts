/**
 * Escapes HTML special characters to prevent XSS when interpolating
 * user-supplied values into HTML templates (e.g., invitation emails).
 */
export function escapeHtml(str: string): string {
  return str
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&#39;');
}
